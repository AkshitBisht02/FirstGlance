export function getNodeType(name) {
  const n = name.toLowerCase();
  if (n.includes('gate')) return 'gate';
  if (n.includes('hostel')) return 'hostel';
  if (n.includes('cafe') || n.includes('canteen') || n.includes('tuck') || n.includes('coffee')) return 'cafeteria';
  if (n.includes('court') || n.includes('ground')) return 'sports';
  if (n.includes('lab')) return 'lab';
  if (n.includes('library')) return 'library';
  if (n.includes('road point')) return 'road';
  if (n.includes('block') || n.includes('department') || n.includes('mca')) return 'building';
  return 'other';
}
